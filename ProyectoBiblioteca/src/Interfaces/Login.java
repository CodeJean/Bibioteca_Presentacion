    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // BTN INGRESAR
        //0 = administrador
        //1 = Usuario
        //2 = Cliente
        int tipo = cbtipo.getSelectedIndex();
        String RESPUES;
        if(tipo == 0){
            RESPUES = obj.LoginEmp(txtuser.getText(),String.valueOf(txtpass.getPassword()) ,String.valueOf(tipo));
            // 0= Clave Incorrecta   1= Correcta
            if (RESPUES.equals("0")) {                 
                JOptionPane.showMessageDialog(null, "USUARIO O CLAVE INCORRECTA, INTENTE NUEVAMENTE", "ADVERTENCIA", JOptionPane.QUESTION_MESSAGE);
            } else {
                LibroMenu menu= new LibroMenu(); // Frame general con todas las opciones
                menu.setVisible(true);
                this.setVisible(false);   
            }            
        }
        if(tipo == 1){
            RESPUES = obj.LoginEmp(txtuser.getText(),String.valueOf(txtpass.getPassword()),String.valueOf(tipo));
            // 0= Clave Incorrecta   1= Correcta
            if (RESPUES.equals("0")) {                 
                JOptionPane.showMessageDialog(null, "USUARIO O CLAVE INCORRECTA, INTENTE NUEVAMENTE", "ADVERTENCIA", JOptionPane.QUESTION_MESSAGE);
            } else {
                LibroMenu jFrame= new LibroMenu(); // Frame general con todas las opciones
                jFrame.setVisible(true);
                this.setVisible(false);   
            }
        }
        if(tipo == 2){//Tipo Cliente
            RESPUES = obj.LoginPer(txtuser.getText(),txtpass.getText());        
            // 0= Clave Incorrecta   1= correcta
            if (RESPUES.equals("0")) {                 
                JOptionPane.showMessageDialog(null, "USUARIO O CLAVE INCORRECTA, INTENTE NUEVAMENTE", "ADVERTENCIA", JOptionPane.QUESTION_MESSAGE);
            } else {
                Registro_Alumno jFrame= new Registro_Alumno(); // Abrir otro Menu
                jFrame.setVisible(true);
                this.setVisible(false);   
            }
        }
        
        
    }//GEN-LAST:event_btnIngresarActionPerformed

